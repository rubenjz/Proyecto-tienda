    private void LoadProducts() {
        try {
            DAOProducto dao = new DAOProductoImpl();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            /*
            otra forma de entender el for each de abajo es esta:
            List<Producto> productos = dao.listar();
            for (Producto producto : productos) {
                Object[] rowData = new Object[]{
                    producto.getId_Producto(), producto.getCodigo(), producto.getNombre(),
                    producto.getCantidad(), producto.getPrecio_venta(), producto.getCosto_compra()
                };
                model.addRow(rowData);
            }
            */
            dao.listar().forEach((producto) -> model.addRow(new Object[]{
                producto.getId_Producto(), producto.getCodigo(), producto.getNombre(),
                producto.getCantidad(), producto.getPrecio_venta(), producto.getCosto_compra()}));
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar productos\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    
